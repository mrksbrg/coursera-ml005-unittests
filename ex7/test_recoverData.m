function test_recoverData()
% 5 / 5	Recover Data (recoverData.m)

    epsilon = 1e-4;

    Z = reshape(cos(1:20), 5, 4)(:,1:3);
    U = reshape(sec(1:16), 4, 4);
    K = 3;
    X_rec = recoverData(Z, U, K);
    a = [4.38005    -0.30362     1.72784    -7.42046
        0.96137     0.77948   192.09174    -3.54480
        -3.34119     1.14593   205.84738     3.58994
        -4.57187     0.45882    30.34789     7.42410
        -1.59920    -0.65013  -173.05331     4.43258];
    assert(X_rec, a, epsilon);

    Z = reshape(tan(1:10), 5, 2);
    U = reshape(sec(1:25), 5, 5);
    K = 2;
    X_rec = recoverData(Z, U, K);
    a = [2.57940   -4.12845    0.42689   -2.06327    5.83718
        -3.13651    6.40656   -3.78220    2.38641   -8.74155
        -7.34560   -8.67682   46.87739    7.68102    7.60133
        1.67184   -3.38221    1.93917   -1.27490    4.62076
        -5.58145    8.98338   -1.04140    4.46020  -12.69011];
    assert(X_rec, a, epsilon);

endfunction
